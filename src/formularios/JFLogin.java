    private boolean validarUsuario() {
        boolean result = false;
        try {
            String usuario = jtf_usuario.getText();
            String clave = "";
            char clave_tmp[] = jpf_clave.getPassword();
            for (int i = 0; i < clave_tmp.length; i++) {
                clave += clave_tmp[i];
            }
            clave = md5(clave);
            sql = "Select count(usuario) as existe from usuarios where usuario='" + usuario + "' and clave='" + clave + "';";
            System.out.println(sql);
            rs = st.executeQuery(sql);
            rs.next();
            int existe = rs.getInt("existe");
            if (existe == 1) {
                JFMenu jf_menu = new JFMenu();
                jf_menu.setLocationRelativeTo(null); //centra el JFrame
                jf_menu.setVisible(true);
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(this, "Usuario o Contaseña incorrectos");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error 101: " + ex.getMessage());
        }
        return result;
    }

    private String md5(String clave) {
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte array[] = md.digest(clave.getBytes());
            StringBuilder sb = new StringBuilder();
            for (int i = 0; i < array.length; ++i) {
                sb.append(Integer.toHexString((array[i] & 0xFF) | 0x100).substring(1, 3));
            }
            return sb.toString();
        } catch (NoSuchAlgorithmException ex) {
            JOptionPane.showMessageDialog(this, "Error 102: " + ex.getMessage());
            return "";
        }
    }

